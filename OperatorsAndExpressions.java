

public class OperatorsAndExpressions {
    public static void main(String[] args) {
        String singleQuote = "It\'s a sunny day."; // Using single quote
        String doubleQuote = "He said, \"Hello!\""; // Using double quote
        String backslash = "\"This\" is\'s  \\ss\\ a bac\nkslash: \\"; // Using backslash
        String newLine = "Hello,\nWorld!"; // Using new line
        String tab = "Name:\tJohn"; // Using tab
        String unicode = "Unicode character: \u03c9"; // Using Unicode (Î©)

        // Printing the strings
        System.out.println(singleQuote);
        System.out.println(doubleQuote);
        System.out.println(backslash);
        System.out.println(newLine);
        System.out.println(tab);
        System.out.println(unicode);
    }
}